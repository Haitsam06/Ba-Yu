const langs = ['en', 'id', 'de', 'es', 'fr', 'ja', 'ko', 'pt', 'ru', 'zh'];

langs.forEach(lang => {
    const filePath = path.join(localesDir, `${lang}.json`);
    
    if (fs.existsSync(filePath)) {
        try {
            const fileData = fs.readFileSync(filePath, 'utf8');
            let json = JSON.parse(fileData);
            
            // Merge settings
            json.privacy_settings = translations[lang].privacy_settings;
            json.security = translations[lang].security;
            
            fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf8');
            console.log(`Updated ${lang}.json for privacy_settings and security`);
        } catch (err) {
            console.error(`Error processing ${lang}.json:`, err);
        }
    } else {
        console.warn(`File not found: ${filePath}`);
    }
});
