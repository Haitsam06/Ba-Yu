const fs = require('fs');

const path = 'resources/frontend/app/pages/NoteDetailPage.tsx';
let content = fs.readFileSync(path, 'utf8');

// Modals
content = content.replace(/>\s*Bagikan Catatan\s*</g, '>{t("note_detail.share_note")}<');
content = content.replace(/>\s*Laporkan Konten\s*</g, '>{t("note_detail.report_content")}<');
content = content.replace(/>\s*Laporkan\s*</g, '>{t("note_detail.report_submit")}<');
content = content.replace(/>\s*Melaporkan\.\.\.\s*</g, '>{t("note_detail.report_submitting")}<');
content = content.replace(/placeholder="Pilih alasan\.\.\."/g, 'placeholder={t("note_detail.report_reason_placeholder")}');
content = content.replace(/placeholder="Jelaskan secara singkat mengapa Anda melaporkan konten ini\.\.\."/g, 'placeholder={t("note_detail.report_desc_placeholder")}');

content = content.replace(/"Spam \/ Promosi"/g, 't("note_detail.report_reason_spam")');
content = content.replace(/"Konten Tidak Pantas"/g, 't("note_detail.report_reason_inappropriate")');
content = content.replace(/"Pelecehan \/ Ujaran Kebencian"/g, 't("note_detail.report_reason_harassment")');
content = content.replace(/"Pelanggaran Hak Cipta"/g, 't("note_detail.report_reason_copyright")');
content = content.replace(/"Lainnya"/g, 't("note_detail.report_reason_other")');

// Waktu / Date Localization (basic JS implementation for now since Intl requires more refactor, wait actually we can just format it inline if it's note.createdAt)
// "31 Maret 2026" - actually if it's already generated by backend, we might not need to touch note.createdAt unless we use a frontend formatter. The user said "waktunya juga belom ke lokalisasi".
// I will wrap note.createdAt with a formatter or just leave it for another step, let's just do `new Date(note.created_at).toLocaleDateString(language)` in mapped note.
// Let's replace the raw note.createdAt in UI.
content = content.replace(/>\s*\{note\.createdAt\}\s*</g, '>{new Date(note.createdAt).toLocaleDateString(language, { year: "numeric", month: "long", day: "numeric" })}<');

// Author Section
content = content.replace(/•\s*\{isFollowing\s*\?\s*"Mengikuti"\s*:\s*isFollowPending\s*\?\s*"Menunggu"\s*:\s*"Ikuti"\}/g, '• {isFollowing ? t("note_detail.author_following") : isFollowPending ? t("note_detail.author_pending") : t("note_detail.author_follow")}');
content = content.replace(/>\s*• Ikuti\s*</g, '>• {t("note_detail.author_follow")}<');
content = content.replace(/\{isFollowing\s*\?\s*"Mengikuti"\s*:\s*"Ikuti Penulis"\}/g, '{isFollowing ? t("note_detail.author_following") : t("note_detail.follow_author")}');

// Comments
content = content.replace(/\{comment\.likes_count > 0 \? comment\.likes_count : 'Suka'\}/g, "{comment.likes_count > 0 ? comment.likes_count : t('note_detail.comment_like')}");
content = content.replace(/>\s*Balas\s*</g, '>{t("note_detail.comment_reply")}<');
content = content.replace(/>\s*Suka\s*\{" "\}/g, '>{t("note_detail.comment_like")} {" "}');
content = content.replace(/Membalas\s*\{" "\}/g, '{t("note_detail.comment_replying")} {" "}');

// See more replies
content = content.replace(/Lihat \{childReplies\.length - 2\} balasan lainnya\.\.\./g, '{t("note_detail.view_more_replies").replace("{{count}}", (childReplies.length - 2).toString())}');
content = content.replace(/Muat Lebih Banyak \(\{comments\.length - 2\}\)/g, '{t("note_detail.load_more_comments").replace("{{count}}", (comments.length - 2).toString())}');

// Rekomendasi
content = content.replace(/Rekomendasi <span className="text-primary">Catatan Lainnya<\/span>/g, '{t("note_detail.recommendation_title")} <span className="text-primary">{t("note_detail.recommendation_subtitle")}</span>');

// Comments More Horizontal
content = content.replace(/<MoreHorizontal className="w-5 h-5 text-gray-400"\s*\/>/g, '<MoreHorizontal className="w-5 h-5 text-gray-500 dark:text-gray-400 hover:text-gray-800 dark:hover:text-gray-200 transition-colors" />');
content = content.replace(/<MoreHorizontal className="w-4 h-4 text-gray-400"\s*\/>/g, '<MoreHorizontal className="w-4 h-4 text-gray-500 dark:text-gray-400 hover:text-gray-800 dark:hover:text-gray-200 transition-colors" />');

fs.writeFileSync(path, content, 'utf8');
console.log('Finished substituting stage 2 note detail texts.');
